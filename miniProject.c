	RCC->APB2ENR|=1<<6;
	GPIOE->CRL&=0xFFFFF0FF;
	GPIOE->CRL|=0x00000800;			//set key2
	GPIOE->ODR|=1<<2;
	RCC->APB2ENR|=0x01;
	AFIO->EXTICR[0]&=0xFFFFF0FF;
	AFIO->EXTICR[0]|=0x00000400;
	EXTI->IMR|=1<<2;
	EXTI->FTSR|=1<<2;
	EXTI->RTSR|=1<<2;
	
